using Carrigan.FileTypeValidators.MimeTypeDefinitions.Images;


namespace Carrigan.FileTypeValidators.Tests.MimeTypeDefinitions.Images;

//IGNORE SPELLING: png pwn

public class ImagePngDefinitionTests
{
    [Fact]
    public void ExactTest()
    {
        ImagePngDefinition definition = new();
        Assert.True(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], "png"));
    }

    [Fact]
    public void Exact_Plus_Extra()
    {
        ImagePngDefinition definition = new();
        Assert.True(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], "png"));
    }

    [Fact]
    public void To_Small()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], "png"));
    }
    [Fact]
    public void Wrong_Extension()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], "pwn"));
    }
    [Fact]
    public void No_Extension()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], ""));
    }
    [Fact]
    public void Empty()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([], "png"));
    }
    [Fact]
    public void Bad_Value()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x80, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], "png"));
    }
    [Fact]
    public void Bad_Value2()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x89, 0x00, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82], "png"));
    }
    [Fact]
    public void Bad_Value3()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x02], "png"));
    }
    [Fact]
    public void Bad_Value24()
    {
        ImagePngDefinition definition = new();
        Assert.False(definition.IsValid([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x00, 0x82], "png"));
    }
}
